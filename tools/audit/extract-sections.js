#!/usr/bin/env node
// Section-aware splitter for index.html. Walks every <script> block and
// breaks it on the section dividers the source already uses (lines like
// `// ═══════════════════════════════════════════════════════════════════════════`
// followed by a label comment). Each labeled section becomes one file under
// `tools/audit/sections/`.
//
// PURPOSE: make audits and code review tractable on a 17,500-line file.
// Subagents that get confused by line numbers in the giant file can read
// individual sections; AI code review can comment on sections in isolation.
// The extracted files are NOT a new source of truth — they're regenerated
// on demand from index.html and gitignored.
//
// To consume: `node tools/audit/extract-sections.js`. The output dir is
// wiped and recreated each run.

'use strict';
const fs = require('fs');
const path = require('path');

const REPO = path.resolve(__dirname, '..', '..');
const INDEX = path.join(REPO, 'index.html');
const OUT_DIR = path.join(__dirname, 'sections');

const html = fs.readFileSync(INDEX, 'utf8');
const scriptRe = /<script>([\s\S]*?)<\/script>/g;
const dividerRe = /^\/\/ [═=]{20,}\s*$/;

// Reset output dir
if (fs.existsSync(OUT_DIR)) fs.rmSync(OUT_DIR, { recursive: true });
fs.mkdirSync(OUT_DIR, { recursive: true });

let blockIdx = 0;
let totalSections = 0;
let m;
while ((m = scriptRe.exec(html)) !== null) {
  blockIdx++;
  const blockStartLine = html.slice(0, m.index).split('\n').length + 1;
  const lines = m[1].split('\n');

  // Walk the block. State: collecting lines into the current section. When
  // we see DIVIDER + LABEL + DIVIDER, that's the start of a new section.
  let curName = `block${blockIdx}_prelude`;
  let curStart = blockStartLine;
  let curBuf = [];
  let i = 0;
  while (i < lines.length) {
    const line = lines[i];
    if (
      dividerRe.test(line) &&
      i + 2 < lines.length &&
      lines[i + 1].startsWith('//') &&
      !dividerRe.test(lines[i + 1]) &&
      dividerRe.test(lines[i + 2])
    ) {
      // Flush previous section
      flush(curName, curStart, curBuf);
      const labelLine = lines[i + 1];
      const labelText = labelLine.replace(/^\/\/\s*/, '').trim();
      curName = labelToFilename(labelText);
      curStart = blockStartLine + i;
      curBuf = [line, lines[i + 1], lines[i + 2]];
      i += 3;
      totalSections++;
      continue;
    }
    curBuf.push(line);
    i++;
  }
  flush(curName, curStart, curBuf);
}

console.log(`extract-sections: ${blockIdx} <script> block(s), ${totalSections} labeled sections -> ${OUT_DIR}`);

function flush(name, startLine, buf) {
  if (buf.length === 0) return;
  // Skip empty/whitespace-only sections (some preludes are empty)
  if (buf.join('').trim() === '') return;
  const banner = [
    `// ============================================================================`,
    `// SOURCE: index.html line ${startLine}`,
    `// SECTION: ${name}`,
    `// AUTOGENERATED by tools/audit/extract-sections.js — DO NOT EDIT.`,
    `// Edit index.html and re-run the extractor.`,
    `// ============================================================================`,
    ''
  ].join('\n');
  // Disambiguate filename collisions (rare but possible if two sections
  // produce the same slug)
  let outName = name + '.js';
  let n = 2;
  while (fs.existsSync(path.join(OUT_DIR, outName))) {
    outName = name + '_' + n + '.js';
    n++;
  }
  fs.writeFileSync(path.join(OUT_DIR, outName), banner + buf.join('\n'));
}

function labelToFilename(label) {
  // Drop leading punctuation, take first ~50 chars of meaningful content
  const cleaned = label
    .replace(/^[^A-Za-z0-9]+/, '')
    .replace(/[^A-Za-z0-9]+/g, '_')
    .replace(/_+/g, '_')
    .replace(/^_|_$/g, '')
    .slice(0, 60);
  return cleaned.toLowerCase() || 'unnamed';
}
